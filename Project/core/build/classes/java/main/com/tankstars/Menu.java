package com.tankstars;

public class Menu {

    private float pos_x;
    private float pos_y;

    private float width;
    private float height;


    public void setposx(float pos_x) {
        this.pos_x = pos_x;
    }

    public void setposy(float pos_y) {
        this.pos_y = pos_y;
    }

    public void setwidth(float width) {
        this.width = width;
    }

    public void setheight(float height) {
        this.height = height;
    }
}
